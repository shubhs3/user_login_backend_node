class User {

    constructor(fname, lname , email , mobile , password , city , address ) {
      this.fname = fname;
      this.lname = lname;
      this.email = email;
      this.mobile = mobile;
      this.password =password;
      this.city = city;
      this.address = address;
    }
    constructor() {
        this.fname = undefined;
        this.lname = undefined;
        this.email = undefined;
        this.mobile = undefined;
        this.password =undefined;
        this.city = undefined;
        this.address = undefined;
      }
  
  }
